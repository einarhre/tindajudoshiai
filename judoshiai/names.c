#define HASH_MASK 0xFFFFFFFFL
#define CRC_MAX 256

static guint64 crc_table[CRC_MAX];
static gboolean table_generated = FALSE;

static void gen_table(void)                /* build the crc table */
{
    guint64 crc, poly;
    gint i, j;

    if (table_generated)
        return;

    poly = 0xEDB88320L;
    for (i = 0; i < CRC_MAX; i++) {
        crc = (guint64)i;
        for (j = 8; j > 0; j--) {
            if (crc & 1)
                crc = (crc >> 1) ^ poly;
            else
                crc >>= 1;
        }
        crc_table[i] = crc;
    }

    table_generated = TRUE;
}

static guint64 hash(gchar *name)
{
    guint64 crc = 0L;
    crc = crc ^ 0xFFFFFFFFL;
    gchar       *uppercase = g_utf8_strup(name, -1);
    gunichar    *name_ucs4 = g_utf8_to_ucs4_fast(uppercase, -1, NULL);
    gunichar    *ch = name_ucs4;

    while (*ch > 0) {
        crc = (crc>>8) ^ crc_table[(crc^(*ch)) & 0xFF];
        ch++;
    }

    g_free(uppercase);
    g_free(name_ucs4);

    return (crc & HASH_MASK);
}

static char buf[100];

gint find_gender(const gchar *name, const gchar *country)
{
    guint64 h;
    gint i, res = 0;
    gchar *cntry = "   ";

    for (i = 0; name[i] && i < (sizeof(buf)-1); i++) {
        buf[i] = name[i];
        if (name[i] == '-' || name[i] == ' ')
            break;
    }

    buf[i] = 0;

    gen_table();
    h = hash(buf);

    cntry = NULL;
    if (country) {
        gchar *cnt = malloc((3*4+1)*sizeof(gchar));
        gchar *cnt_dwn = g_utf8_strdown(g_utf8_strncpy(cnt, country, MIN(3,g_utf8_strlen(country, -1))), -1);
        g_free(cnt);
        if (
            !g_strcmp0(cnt_dwn,"isl") || !g_strcmp0(cnt_dwn,"is") ||
            !g_strcmp0(cnt_dwn,"ísl") || !g_strcmp0(cnt_dwn,"ice")
        ) {
            cntry = "ISL";
        } else if (
            !g_strcmp0(cnt_dwn,"fin") || !g_strcmp0(cnt_dwn,"fi")
        ) {
            cntry = "FIN";
        } else {
        }
        g_free(cnt_dwn);
    }

    if (!cntry || g_strcmp0(cntry,"ISL") == 0) {
        for (i = 0; i < NUM_IS_MEN; i++) {
            if (h == men_is_names[i]) {
                res |= IS_MALE;
                break;
            }
        }

        for (i = 0; i < NUM_IS_WOMEN; i++) {
            if (h == women_is_names[i]) {
                res |= IS_FEMALE;
                break;
            }
        }
    }
    if (!cntry || g_strcmp0(cntry,"FIN") == 0) {
        for (i = 0; i < NUM_FIN_MEN; i++) {
            if (h == men_fin_names[i]) {
                res |= IS_MALE;
                break;
            }
        }
        for (i = 0; i < NUM_FIN_WOMEN; i++) {
            if (h == women_fin_names[i]) {
                res |= IS_FEMALE;
                break;
            }
        }
    }

    //mylog("name=%s buf=%s hash=0x%lx res=%d\n", name, buf, h, res);

    return res;
}

